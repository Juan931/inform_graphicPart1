    private void jBotonExportarActionPerformed(java.awt.event.ActionEvent evt) {                                               
           
        HSSFWorkbook book = new HSSFWorkbook();
            HSSFSheet sheet = book.createSheet();
            book.setSheetName(0,"Planes");
            String[] headers = new String[]{
                "ID PLAN",
                "NOMBRE",
                "DESCRIPCIÓN",
                "PRECIO"
            };
            CellStyle headerCellStyle = book.createCellStyle();
            HSSFFont font = book.createFont();
            font.setBold(true);
            headerCellStyle.setFont(font);
            
            HSSFRow headersRow = sheet.createRow(0);
            for (int i = 0; i<headers.length;i++){
                String header = headers[i];
                HSSFCell cell = headersRow.createCell(i);
                cell.setCellStyle(headerCellStyle);
                cell.setCellValue(header);
            }
           
            
            for (int i = 0;i< reporte.size();i++){
                HSSFRow row = sheet.createRow(i+1);
                int id = reporte.get(i).getId();
                String nombre = reporte.get(i).getNombre();
                String descripcion = reporte.get(i).getDescripcion();
                int precio = reporte.get(i).getPrecio();
                row.createCell(0).setCellValue(id);
                row.createCell(1).setCellValue(nombre);
                row.createCell(2).setCellValue(descripcion);
                row.createCell(3).setCellValue(precio);
            }
           
        try {
            FileOutputStream archivo = new FileOutputStream("ReportePlanes.xls");
            book.write(archivo);
            archivo.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                              
   
