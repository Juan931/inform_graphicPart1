    private void BotonCrearMascotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearMascotaActionPerformed
        String codigo = txtCodigoMascota.getText();
        String nombre = txtNombreMascota.getText();
        String edad = txtEdadMascota.getText();
        String peso = txtPesoMascota.getText();
        String especie = cbEspecie.getSelectedItem().toString();
        Plan plan;
        int indice = cbDueno.getSelectedIndex();
        Cliente dueno = listaClientes.get(indice);
        if (cbPlan.getSelectedItem().toString() == "Bienestar"){
            plan = reporte.get(0);
        }else{
            if (cbPlan.getSelectedItem().toString() == "Élite"){
                plan = reporte.get(1);
            }else plan = reporte.get(2);
        }
        Mascota mascota = new Mascota(codigo,nombre,edad,peso,especie,plan,dueno);
        JOptionPane.showMessageDialog(this, "Los datos de la mascota son: codigo: "+mascota.getCodigo()+ " Nombre: "+ mascota.getNombre()+"Plan: "+ mascota.getPlan().getNombre());
        listaMascotas.add(mascota);
        cbMascota.addItem(mascota.getCodigo()+" "+mascota.getNombre());
    }//GEN-LAST:event_BotonCrearMascotaActionPerformed

