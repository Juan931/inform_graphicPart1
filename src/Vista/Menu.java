    private void botonCrearPlanActionPerformed(java.awt.event.ActionEvent evt) {                                               
        
        String nombre = cbNombrePlan.getSelectedItem().toString();
        String descripcion = txtDescripcionPlan.getText();
        int precio  = Integer.parseInt(txtPrecioPlan.getText());
        
        Plan plan = new Plan(nombre,descripcion,precio);
        if(controlador.CrearPlan(plan)){
                    JOptionPane.showMessageDialog(this, "El plan fue creado exitosamente ");
        }else {
            JOptionPane.showMessageDialog(this, "El plan NOOOOO fue creado ");
        }
        
        
        
    }                                              

